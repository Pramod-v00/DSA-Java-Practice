
/*
Problem: Two Sum
Platform: LeetCode 27
Difficulty: Easy
Topic: Remove Element
*/

class Solution {
    public int removeElement(int[] nums, int val) {
        int i=0;
        for(int j=0;j<nums.length;j++){
            if(nums[j]!=val){
                nums[i]=nums[j];
                i++;
            }
        }
        return i;
    }
}